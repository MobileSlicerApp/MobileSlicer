package com.mobileslicer.profiles

import org.json.JSONObject

internal fun JSONObject.putNativeFilamentConfiguration(filament: FilamentProfile): JSONObject = apply {
    put("filament_diameter", filament.diameterMm.toDouble())
            .put(NativeConfigKeys.Filament.Type, filament.materialType)
            .put("filament_vendor", filament.vendor)
            .put("filament_soluble", filament.soluble)
            .put("filament_is_support", filament.supportMaterial)
            .put(NativeConfigKeys.Filament.ExtruderVariant, filament.filamentExtruderVariant)
            .put(NativeConfigKeys.Filament.SelfIndex, filament.filamentSelfIndex)
            .put("filament_change_length", filament.filamentChangeLengthMm.toDouble())
            .put("required_nozzle_HRC", filament.requiredNozzleHrc)
            .put(NativeConfigKeys.Filament.DefaultColor, filament.defaultFilamentColor)
            .put("filament_adhesiveness_category", filament.adhesivenessCategory)
            .put(NativeConfigKeys.Filament.Density, filament.densityGPerCm3.toDouble())
            .put("filament_shrink", filament.shrinkageXyPercent.toDouble())
            .put("filament_shrinkage_compensation_z", filament.shrinkageZPercent.toDouble())
            .put("filament_cost", filament.costPerKg.toDouble())
            .put("temperature_vitrification", filament.softeningTemperatureC)
            .put("idle_temperature", filament.idleTemperatureC)
            .put("nozzle_temperature_range_low", filament.nozzleTemperatureRangeLowC)
            .put("nozzle_temperature_range_high", filament.nozzleTemperatureRangeHighC)
            .put("filament_flow_ratio", filament.flowRatio.toDouble())
            .put("enable_pressure_advance", filament.pressureAdvanceEnabled)
            .put(NativeConfigKeys.Filament.PressureAdvance, filament.pressureAdvance.toDouble())
            .put("pellet_flow_coefficient", filament.pelletFlowCoefficient.toDouble())
            .put("adaptive_pressure_advance", filament.adaptivePressureAdvanceEnabled)
            .put("adaptive_pressure_advance_overhangs", filament.adaptivePressureAdvanceOverhangsEnabled)
            .put("adaptive_pressure_advance_bridges", filament.adaptivePressureAdvanceBridges.toDouble())
            .put("adaptive_pressure_advance_model", filament.adaptivePressureAdvanceModel)
            .put(NativeConfigKeys.Filament.MaxVolumetricSpeed, filament.maxVolumetricSpeedMm3PerSec.toDouble())
            .put("filament_adaptive_volumetric_speed", filament.adaptiveVolumetricSpeedEnabled)
            .put("volumetric_speed_coefficients", filament.volumetricSpeedCoefficients)
            .put("filament_start_gcode", filament.filamentStartGcode)
            .put("filament_end_gcode", filament.filamentEndGcode)
            .put("nozzle_temperature_initial_layer", filament.nozzleTemperatureInitialLayerC)
            .put("nozzle_temperature", filament.nozzleTemperatureC)
            .put("chamber_temperature", filament.chamberTemperatureC)
            .put("activate_chamber_temp_control", filament.activateChamberTemperatureControl)
            .put(NativeConfigKeys.Temperature.SupertackPlateInitialLayer, filament.supertackPlateTemperatureInitialLayerC)
            .put(NativeConfigKeys.Temperature.SupertackPlate, filament.supertackPlateTemperatureC)
            .put(NativeConfigKeys.Temperature.CoolPlateInitialLayer, filament.coolPlateTemperatureInitialLayerC)
            .put(NativeConfigKeys.Temperature.CoolPlate, filament.coolPlateTemperatureC)
            .put(NativeConfigKeys.Temperature.TexturedCoolPlateInitialLayer, filament.texturedCoolPlateTemperatureInitialLayerC)
            .put(NativeConfigKeys.Temperature.TexturedCoolPlate, filament.texturedCoolPlateTemperatureC)
            .put(NativeConfigKeys.Temperature.EngineeringPlateInitialLayer, filament.engineeringPlateTemperatureInitialLayerC)
            .put(NativeConfigKeys.Temperature.EngineeringPlate, filament.engineeringPlateTemperatureC)
            .put(NativeConfigKeys.Temperature.BedInitialLayer, filament.bedTemperatureInitialLayerC)
            .put(NativeConfigKeys.Temperature.Bed, filament.bedTemperatureC)
            .put(NativeConfigKeys.Temperature.HotPlateInitialLayer, filament.bedTemperatureInitialLayerC)
            .put(NativeConfigKeys.Temperature.HotPlate, filament.bedTemperatureC)
            .put(NativeConfigKeys.Temperature.TexturedPlateInitialLayer, filament.texturedPlateTemperatureInitialLayerC)
            .put(NativeConfigKeys.Temperature.TexturedPlate, filament.texturedPlateTemperatureC)
            .put("fan_min_speed", filament.minFanSpeedPercent)
            .put("fan_max_speed", filament.coolingPercent)
            .put("cooling_baseline", filament.coolingPercent)
            .put("close_fan_the_first_x_layers", filament.noCoolingFirstLayers)
            .put("full_fan_speed_layer", filament.fullFanSpeedLayer)
            .put("fan_cooling_layer_time", filament.fanCoolingLayerTimeSeconds.toDouble())
            .put("slow_down_layer_time", filament.slowDownLayerTimeSeconds.toDouble())
            .put("reduce_fan_stop_start_freq", filament.reduceFanStopStartFrequency)
            .put("slow_down_for_layer_cooling", filament.slowDownForLayerCooling)
            .put("dont_slow_down_outer_wall", filament.dontSlowDownOuterWall)
            .put("slow_down_min_speed", filament.slowDownMinSpeedMmPerSec.toDouble())
            .put("enable_overhang_bridge_fan", filament.enableOverhangBridgeFan)
            .put("overhang_fan_threshold", filament.overhangFanThreshold)
            .put("overhang_fan_speed", filament.overhangFanSpeedPercent)
            .put("internal_bridge_fan_speed", filament.internalBridgeFanSpeedPercent)
            .put("support_material_interface_fan_speed", filament.supportMaterialInterfaceFanSpeedPercent)
            .put("ironing_fan_speed", filament.ironingFanSpeedPercent)
            .put("additional_cooling_fan_speed", filament.additionalCoolingFanSpeedPercent)
            .put("activate_air_filtration", filament.activateAirFiltration)
            .put("during_print_exhaust_fan_speed", filament.duringPrintExhaustFanSpeedPercent)
            .put("complete_print_exhaust_fan_speed", filament.completePrintExhaustFanSpeedPercent)
            .put("filament_minimal_purge_on_wipe_tower", filament.minimalPurgeOnWipeTowerMm3.toDouble())
            .put("filament_tower_interface_pre_extrusion_dist", filament.towerInterfacePreExtrusionDistanceMm.toDouble())
            .put("filament_tower_interface_pre_extrusion_length", filament.towerInterfacePreExtrusionLengthMm.toDouble())
            .put("filament_tower_ironing_area", filament.towerIroningAreaMm2.toDouble())
            .put("filament_tower_interface_purge_volume", filament.towerInterfacePurgeVolumeMm.toDouble())
            .put("filament_tower_interface_print_temp", filament.towerInterfacePrintTemperatureC)
            .put("filament_loading_speed_start", filament.loadingSpeedStartMmPerSec.toDouble())
            .put("filament_loading_speed", filament.loadingSpeedMmPerSec.toDouble())
            .put("filament_unloading_speed_start", filament.unloadingSpeedStartMmPerSec.toDouble())
            .put("filament_unloading_speed", filament.unloadingSpeedMmPerSec.toDouble())
            .put("filament_toolchange_delay", filament.toolchangeDelaySeconds.toDouble())
            .put("filament_cooling_moves", filament.coolingMoves)
            .put("filament_cooling_initial_speed", filament.coolingInitialSpeedMmPerSec.toDouble())
            .put("filament_cooling_final_speed", filament.coolingFinalSpeedMmPerSec.toDouble())
            .put("filament_stamping_loading_speed", filament.stampingLoadingSpeedMmPerSec.toDouble())
            .put("filament_stamping_distance", filament.stampingDistanceMm.toDouble())
            .put("filament_ramming_parameters", filament.rammingParameters)
            .put("filament_multitool_ramming", filament.multitoolRamming)
            .put("filament_multitool_ramming_volume", filament.multitoolRammingVolumeMm3.toDouble())
            .put("filament_multitool_ramming_flow", filament.multitoolRammingFlowMm3PerSec.toDouble())
            .put("compatible_printers", filament.compatiblePrinters)
            .put("compatible_printers_condition", filament.compatiblePrintersCondition)
            .put("compatible_prints", filament.compatiblePrints)
            .put("compatible_prints_condition", filament.compatiblePrintsCondition)
            .put("filament_notes", filament.filamentNotes)
}
